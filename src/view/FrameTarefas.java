    //Botão Tarefa Feita faz com que a Tarefa de um Certo Militar seja Coloca tudo em False que significa "FEITO"=False  e Não Feito= "true"
    private void btConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmarActionPerformed
        if(militarAtual != null) {
            String dataStr = ftfAtividadeData.getText();
            if(dataStr.trim().equals("")) {
                JOptionPane.showMessageDialog(null, "A data de confirmação da atividade deve ser preenchida.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            Date data = Utils.stringToDate(dataStr);
            boolean executouAtividade = rbAtividadeSim.isSelected();
            MilitarController.logExecucaoAtividade(militarAtual,executouAtividade,executouAtividade ? 0 : militarAtual.getGrauPenalidade()+1,data);
            
            ftfAtividadeData.setText("");
            buttonGroupExecutouAtividades.clearSelection();
            if(militarAtual.getGrauPenalidade() > 0) {
                rbPenalidades.get(militarAtual.getGrauPenalidade()-1).setSelected(rootPaneCheckingEnabled);
            }
            else {
                buttonGroupPenalidades.clearSelection();
            }
        }
    }//GEN-LAST:event_btConfirmarActionPerformed

    //limpa os campos
    private void limparCampos() {
        tfNome.setText("");
        ftfCpf.setText("");
        ftfRg.setText("");
        ftfNascimento.setText("");
        tfIdade.setText("");
        cbEstado.setSelectedIndex(-1);
        tfSexo.setText("");
        for(javax.swing.JCheckBox c : checkBoxes) {
            c.setSelected(false);
        }
        buttonGroupPenalidades.clearSelection();
        btConfirmar.setEnabled(false);
        btLogAtividades.setEnabled(false);
    }
    
    private void setCamposFromMilitar(Militar militar) {
        tfCodigo.setText("" + militar.getCodigo());
        tfNome.setText(militar.getNome());
        ftfCpf.setText(militar.getCpf());
        ftfRg.setText(militar.getRg());
        cbEstado.setSelectedItem(militar.getEstado());
        tfSexo.setText(militar.getSexo() == 'M' ? "Masculino" : "Feminino");
        ftfNascimento.setText(Utils.dateToString(militar.getNascimento()));
        ImageIcon patenteImagem = new ImageIcon(getClass().getResource("/images/" + militar.getPatente().getImagem()));
        int size = patenteImagem.getIconHeight();
        //lblPatenteIcon.setSize(patenteImagem.getIconWidth(),patenteImagem.getIconHeight());
        lblPatenteIcon.setIcon(patenteImagem);
        lblPatenteIcon.setToolTipText(militar.getPatente().getNome());
        String nascimento = ftfNascimento.getText();
        int idade = Utils.getAgeInYears(Utils.stringToDate(nascimento));
        tfIdade.setText(""+idade);
        for(int i = 0; i < checkBoxes.size(); i++) {
            checkBoxes.get(i).setSelected(militar.fazAtividade(EnumTarefa.values()[i]));
        }
        if(militar.getGrauPenalidade() > 0) {
            rbPenalidades.get(militar.getGrauPenalidade()-1).setSelected(rootPaneCheckingEnabled);
        }
        else {
            buttonGroupPenalidades.clearSelection();
        }
    }
    //Botão Buscar ao apertar ele Buscar o Militar que você Deseja Encontrar através do nome.
    private void BtBuscarMilitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtBuscarMilitarActionPerformed
        String codigoStr = tfCodigo.getText();
        String cpf = ftfCpf.getText();
        String rg = ftfRg.getText();
        String estado = (String)cbEstado.getSelectedItem();

        int tipoBusca = 0;
        if(codigoStr.trim().equals("")) {
            if(cpf.trim().replaceAll("[-./]","").equals("")) {
                tipoBusca = 2;
            }
            else
                tipoBusca = 1;
        }
        
        Militar militar;
        if(tipoBusca == 0) {
            int codigo = Utils.stringToInt(codigoStr);
            militar = MilitarController.buscarMilitar(codigo);
        }
        else if(tipoBusca == 1)
            militar = MilitarController.buscarMilitarCPF(cpf);
        else
            militar = MilitarController.buscarMilitarRG(rg,estado);

        if(militar != null) {
            setCamposFromMilitar(militar);
            ftfAtividadeData.setEnabled(true);
            rbAtividadeSim.setEnabled(true);
            rbAtividadeNao.setEnabled(true);
            btConfirmar.setEnabled(true);
            btLogAtividades.setEnabled(true);
            militarAtual = militar;
        }
        else {
            limparCampos();
            militarAtual = null;
        }
    }//GEN-LAST:event_BtBuscarMilitarActionPerformed
     
