    private void rbAtividadeNaoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_rbAtividadeNaoItemStateChanged
        if(militarAtual == null)
            return;
        
        int grauPenalidade = militarAtual.getGrauPenalidade();
        if(evt.getStateChange() == ItemEvent.SELECTED) {
            grauPenalidade += 1;
            if(grauPenalidade > EnumPenalidade.getCount())
                grauPenalidade = EnumPenalidade.getCount();
        }

        if(grauPenalidade == 0) {
            buttonGroupPenalidades.clearSelection();
        }
        else {
            rbPenalidades.get(grauPenalidade-1).setSelected(true);
        }
    }//GEN-LAST:event_rbAtividadeNaoItemStateChanged

